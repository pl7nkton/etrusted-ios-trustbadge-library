import Foundation
import SwiftUI

/**
 This extension defines custom Trustedshops colors
 */
extension Color {

    /// Trustedshops off white color
    static let tsOffWhite = Color("TsOffWhite")

    /// Trustedshops gray 100 color
    static let tsGray100 = Color("TsGray100")

    /// Trustedshops gray 200 color
    static let tsGray200 = Color("TsGray200")

    /// Trustedshops gray 300 color
    static let tsGray300 = Color("TsGray300")

    /// Trustedshops gray 600 color
    static let tsGray600 = Color("TsGray600")

    /// Trustedshops blue 700 color
    static let tsBlue700 = Color("TsBlue700")
}
